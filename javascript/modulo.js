export class Matematicas{
    constructor(a, b){
        this.a = a
        this.b = b
    }
    suma = () => {
        return this.a + this.b
    }
    resta = () => {
        return this.a - this.b
    }
    multiplicar = () => {
        return this.a + this.b
    }
    dividir = () => {
        return this.a + this.b
    }
}